// Functions
// Function Declaration
function add(a, b) {  // with parameters
    var total = a + b;
    document.write(total);
    return total; // return value parameter
}


function sub() {  // no parameters
    document.write("Hello");
    document.write("Hello");
}

sub();
sub();

add(2, 3);


var total = add(2, 3);  // for return function
document.write(total);


