// Control Statements

// if-else Statements
var a = 5;
if (a > 10) {
    console.log("a is greater than 10");
} else {
    console.log("a is less than or equal to 10");
}

// Switch Statements
var day = "Monday";
switch (day) {
    case "Monday":
        console.log("Today is Monday");
        break;
    case "Tuesday":
        console.log("Today is Tuesday");
        break;
    default:
        console.log("Today is not Monday or Tuesday");
        break;
}

