// Loops

// while loop

var i = 0;
while (i < 10) {  // entry check loop
    console.log(i);
    i++;
}

// do while loop

var i = 11;
do {   // exit check loop
    console.log(i);
    i++;
} while (i < 10);


// for loop

for (var i = 0; i <= 10; i++) {
    console.log(i);
}

